<h1>Les aides pour le logement : </h1>
<ul class="tilesWrap">
    <li>
        <h3>Les aides de la CAF : l’APL ou l’ALS :</h3>
        <p>
            Selon le type de logement que vous occupez, vous pouvez prétendre à deux types d'aide au logement attribuées par la CAF (APL ou ALS). Elles ne sont pas cumulables entre elles, ni avec les allocations familiales des parents. Elles ne sont pas rétroactives.

            L’APL (aide personnalisée au logement) est réservée aux logements qui font l’objet d’une convention avec l’État (cités universitaires, foyers, HLM, etc.).

            L’ALS (allocation de logement à caractère social) vise toutes les autres habitations, sous certaines conditions (surface du logement, WC, chauffage, arrivée d’eau, etc.).

            L’aide varie en fonction du montant du loyer, des ressources dont vous disposez (et non celles de vos parents), de la nature du logement et du lieu d’habitation.

            Attention : si le logement appartient à un membre de votre famille, vous ne pouvez pas en bénéficier. Au contraire, si vous êtes en colocation, chaque locataire peut bénéficier d'une aide de la CAF. Le bail et la quittance doivent par ailleurs être établis à votre nom. Le logement doit aussi être occupé au moins 8 mois par an
        </p>
    </li>
    <li>
        <h3>L'avance loca-pass : </h3>
        <p>
            L’avance loca-pass vous prête à taux 0 le montant du dépôt de garantie demandé par le propriétaire quand vous signez votre bail, dans la limite de 1.200 €. Vous devrez rembourser cette somme à compter du troisième mois après le versement de l'avance, durant 25 mois maximum, avec des remboursements d'au moins 20 € par mois.

            Cette aide peut être accordée quel que soit le logement, qu'il appartienne à un propriétaire privé ou à un organisme social. Elle est ouverte aux jeunes de moins de 30 ans et aux salariés du secteur privé non agricole.
        </p>
    </li>
    <li>
        <h3>La garantie Visale : </h3>
        <p>
            Depuis fin mai 2018, tous les étudiants peuvent bénéficier du dispositif Visale, qui se porte caution pour votre logement pendant toute la durée de son occupation. L’avantage de ce service géré par Action Logement : plus besoin de demander à vos parents d’être vos garants (sollicités en cas d’impayés). Gratuite, la démarche est dématérialisée, tout se fait sur Internet.
        </p>
    </li>
    <li>
        <h3>Des aides locales : </h3>
        <p>
            Renseignez-vous auprès de votre ville, votre CROUS (centre régional des œuvres universitaires et scolaires), votre département, votre région, ou même votre établissement, sur l’existence d’aides supplémentaires.
        </p>
    </li>
    <li>
        <h3>Les aides alimentaires d’urgence : </h3>
        <p>
            Les CROUS offrent aux étudiants en situation de précarité une aide alimentaire. En fonction de ses besoins, l’étudiant peut bénéficier d’une aide sous plusieurs formes :
            - tickets RU,
            - rechargements de Pass Moneo
            - délivrance d’espèces.

            La procédure est engagée sans délai : la demande de l’étudiant est immédiatement traitée et il reçoit une avance. Le passage devant une commission ad hoc se fait a posteriori.
        </p>
    </li>

</ul>

