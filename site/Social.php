        <p>
            Le réseau du Crous compte plus de 175 assistantes sociales qui effectuent des permanences du lundi au vendredi. Elles disposent pour les étudiants de logements temporaires dans certains CROUS, de logements provenant de conventions avec différents partenaires et s’emploient ensuite à leur fournir un logement, définitif, pour l’année universitaire.

            De nombreux styles d’hébergement sont recensés dans plusieurs villes de France comme Lille, Nice, Montpellier, Toulouse... Les logements de dépannage peuvent être entièrement pris en charge par le Crous, mais ils peuvent aussi être payants pour l’étudiant (de 5 à 20 euros la nuit selon les endroit).

            Comme leur nom l’indique, ces logements d’urgence sont temporaires. Pour obtenir un logement Crous, vous devez absolument monter un dossier social étudiant. Vous devez vous y prendre tôt, dès le mois de Janvier, pour mettre toutes les chances de votre côté.
